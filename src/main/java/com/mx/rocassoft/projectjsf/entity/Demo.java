package com.mx.rocassoft.projectjsf.entity;

/**
 * @author ROCASS
 * Clase demo para consumir API
 *
 */
public class Demo {
	
	/**
	 * Clase con nombre, email y rfc que se extraen de API.
	 *
	 */
	
	private String nombre;
	private String email;
	private String rfc;
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the rfc
	 */
	public String getRfc() {
		return rfc;
	}
	/**
	 * @param rfc the rfc to set
	 */
	public void setRfc(String rfc) {
		this.rfc = rfc;
	}
	
	
}
